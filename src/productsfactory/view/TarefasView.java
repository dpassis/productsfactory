private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
// TODO add your handling code here:
    if (this.validaCampos()) {
        int option = JOptionPane.showConfirmDialog(this, "Confirma atualização?", "Tarefas", JOptionPane.YES_NO_OPTION, 3);

        if (option == 0) {

           
            if (TarefasControl.atualizarTarefa(this.getDataTarefa())) {
                try {
                    List<Tarefa> listTarefas = TarefasControl.listaTarefasData(this.dtDataInicio.getDate(), this.dtDataTermino.getDate(), user);
                    tableTarefas(listTarefas);
                    JOptionPane.showMessageDialog(this, "Tarefa Atualizada com sucesso", "Tarefas", 1);
                    //ControleUsuario.insereAcoesUsuario("Atualizou a tarefa : " + this.tfDescricaTarefa.getText() + "", user);
                } catch (SQLException ex) {
                    Logger.getLogger(TarefasView.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Tarefa não pode ser atualizada.", "Tarefas", 0);
            }
        }
    }


}//GEN-LAST:event_btnAtualizarActionPerformed

private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
// TODO add your handling code here:
    if (user.getNomeUsuario().equals(usuarioDono)) {
       
            int option = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir essa tarefa?", "Tarefas", JOptionPane.YES_NO_OPTION, 3);

            if (option == 0) {
                 if (TarefasControl.excluirTarefa(this.getDataTarefa())) {

                try {
                    JOptionPane.showMessageDialog(this, "Tarefa excluída com sucesso!", "Tarefas", 1);
                   // ControleUsuario.insereAcoesUsuario("Excluiu a tarefa : " + this.tfDescricaTarefa.getText() + "", user);
                    this.limpaCampos();
                    List<Tarefa> listTarefas = null;
                    this.tableTarefas(listTarefas);
                } catch (SQLException ex) {
                    Logger.getLogger(TarefasView.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Tarefa não pode ser excluída!", "Tarefas", 0);
            }

        } else {
             JOptionPane.showMessageDialog(this, "Exclusão cancelada pelo usuário.", "Tarefas", 1);
           
            
        }
    } else {
        JOptionPane.showMessageDialog(this, "Somente o usuário que criou essa tarefa poderá fazer sua exclusão.", "Tarefas", 1);
    }

}//GEN-LAST:event_btnExcluirActionPerformed

