    /**
     * Método utilizado para validar alguns campos 
     * do formulário
     * @return boolean
     */
    private boolean validaCampos() {

        Integer cont = 0;

        if (this.tfDescricaTarefa.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Descrição da tarefa obrigatória!", "Tarefas", 2);
            cont += 1;
        }

        if (this.dtDataTarefa.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Data data tarefa obrigatória!", "Tarefas", 2);
            cont += 1;
        }

        if (this.ftfHoraTarefa.getText().equals("__:__")) {
            JOptionPane.showMessageDialog(this, "Valor da despesa inválido!", "Tarefas", 2);
            cont += 1;
        }


        if (cont == 0) {
            return true;
        } else {
            return false;
        }
    }

    private void populaComboUsuario() throws SQLException {

        //assim como é feito numa JTable, temos o DefaultComboBoxModel que é o model do JComboBox
        DefaultComboBoxModel comboModel = (DefaultComboBoxModel) this.cmbUsuario.getModel();
        //removendo todos os elementos do combo
        comboModel.removeAllElements();
        //cria a lista: java.util.List
        List<Usuario> usuario = new ArrayList<Usuario>();

        usuario = UsuarioControl.ComboUsuariosTarefa();

        if (usuario != null) {

            for (int i = 0; i <= usuario.size() - 1; i++) {
                Usuario userCombo = usuario.get(i);
                comboModel.addElement(userCombo);
            }
        }


    }

