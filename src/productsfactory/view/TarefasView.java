private void tblTarefasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTarefasMouseClicked
// TODO add your handling code here:
    if (evt.getClickCount() == 1) {

        codigoTarefa = Integer.valueOf(tblTarefas.getValueAt(tblTarefas.getSelectedRow(), 0).toString());
        this.tfDescricaTarefa.setText(tblTarefas.getValueAt(tblTarefas.getSelectedRow(), 1).toString());
        this.dtDataTarefa.setDate(DataUteis.sdfParse(tblTarefas.getValueAt(tblTarefas.getSelectedRow(), 2).toString()));
        this.ftfHoraTarefa.setText(tblTarefas.getValueAt(tblTarefas.getSelectedRow(), 3).toString());
        this.cmbStatusTarefa.setSelectedItem(tblTarefas.getValueAt(tblTarefas.getSelectedRow(), 4).toString());
        usuarioDono = tblTarefas.getValueAt(tblTarefas.getSelectedRow(), 5).toString();
        //testando valor da comboCargo
        for (int index = 0; index < cmbUsuario.getItemCount(); index++) {
            if (cmbUsuario.getItemAt(index).toString().equals(tblTarefas.getValueAt(tblTarefas.getSelectedRow(), 6).toString())) {
                cmbUsuario.setSelectedIndex(index);
                break;
            }
        }
        if(!user.getNomeUsuario().equals(usuarioDono)){
            
            this.tfDescricaTarefa.setEditable(false);
            this.ftfHoraTarefa.setEditable(false);
            this.dtDataTarefa.setEnabled(false);
            this.cmbUsuario.setEnabled(false);
            
        }else{
            
            this.tfDescricaTarefa.setEditable(true);
            this.ftfHoraTarefa.setEditable(true);
            this.dtDataTarefa.setEnabled(true);
            this.cmbUsuario.setEnabled(true);
        }
        this.btnAtualizar.setVisible(true);
        this.btnExcluir.setVisible(true);
        this.btnSalvar.setVisible(false);


    }
}//GEN-LAST:event_tblTarefasMouseClicked

