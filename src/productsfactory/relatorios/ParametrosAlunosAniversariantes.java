    private void selecionaMes(){
        
        
        int intMes = this.dtMes.getMonth()+1;
        String month = String.valueOf(intMes);
        String mesNumber;
        String mesChar = "";
        if (intMes > 0 && intMes <10){
            mesNumber = new StringBuffer().append("0").append(month).toString();
        }
        else {
            mesNumber = month;
        }
        
        if(mesNumber.equals("01")){
            mesChar = "Janeiro";   
        }else if( mesNumber.equals("02")){
            mesChar = "Fevereiro";
        }else if( mesNumber.equals("03")){
            mesChar = "Março";
        }else if( mesNumber.equals("04")){
            mesChar = "Abril";
        }else if( mesNumber.equals("05")){
            mesChar = "Maio";
        }else if( mesNumber.equals("06")){
            mesChar = "Junho";
        }else if( mesNumber.equals("07")){
            mesChar = "Julho";
        }else if( mesNumber.equals("08")){
            mesChar = "Agosto";
        }else if( mesNumber.equals("09")){
            mesChar = "Setembro";
        }else if( mesNumber.equals("10")){
            mesChar = "Outubro";
        }else if( mesNumber.equals("11")){
            mesChar = "Novembro";
        }else if( mesNumber.equals("12")){
            mesChar = "Dezembro";
        }

        System.out.println(mesChar);
        
        RelatorioAlunosAniversariantes openRelatorio = new RelatorioAlunosAniversariantes(this, rootPaneCheckingEnabled);
        this.dispose();
        openRelatorio.geraRelatorio(mesNumber,mesChar);
    }


