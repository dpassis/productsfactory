public class Offers {
    
    
    private String socCD;
    private String socName;
    private String statusOffer;


    public String getSocCD() {
        return socCD;
    }

    public void setSocCD(String socCD) {
        this.socCD = socCD;
    }

    public String getSocName() {
        return socName;
    }

    public void setSocName(String socName) {
        this.socName = socName;
    }
    
    public String getStatusOffer() {
        return statusOffer;
    }

    public void setStatusOffer(String statusOffer) {
        this.statusOffer = statusOffer;
    }
    
}
